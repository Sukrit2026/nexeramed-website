<div class="hdtitlemain">ìContagionî Scenario: Nexera Medical On Leading Edge Of Airborne Microbe
Protection With Its Next Generation Surgical Respirator Mask
</div><br>

<strong>FORT LAUDERDALE, Florida (September 15, 2011)</strong> ñ Last Friday, September 9th, the movie
ìContagionî hit theaters with its story built around an airborne virus that quickly becomes a
global pandemic and decimates mankind. While the level of outbreak in the movie is certainly a
worst-case scenario, recent outbreaks of the swine and bird flu have shown that airborne
viruses are a real threat.
<br /><br />

Currently, the medical community is working to identify, understand and stop existing and
emerging airborne pathogenic microbes, however, one of the best ways to protect oneself from
airborne microbe threats is to prevent the microbes from entering oneís respiratory system in
the first place.
<br /><br />

Aside from frequent hand washing, staying away from crowds and avoiding people who are
sick, wearing a respirator facemask is one of the most affordable and easiest ways to limit the
spread of harmful airborne microbes. Wearing respirators is part of the protocol designed by
the WHO and CDC to protect people against harmful airborne diseases, and respirators are an
integral part of Personal Protective Equipment (ìPPEî) as described by medical professionals.
On September 8th, Nexera Medical, Inc. (ìNexeraî) announced plans to submit to the U.S. Food
and Drug Administration (FDA) amended antimicrobial claims for its current 510(k) clearance
for the SpectraShieldô 9500 Surgical N95 Respirator with embedded antimicrobial technology.
The expanded antimicrobial claims will be supported by test data on the inactivation
performance of certain strains of viruses recognized by the FDA to cause various types of
influenzas and colds considered public health issues, including the H1N1 (Swine Flu) Virus,
H5N1 (Bird Flu) Virus, Rhino Virus (common cold), and SARS (Severe Acute Respiratory
Syndrome).
<br /><br />

These tests are very similar to those tests that have already been conducted by Nexera in the
European Union. There, the SpectraShieldô Respirator Mask demonstrated its ability to kill
these strains of viruses and was certified as an antimicrobial FFP3 respirator mask and has
been issued a CE Mark by INSPEC, the applicable jurisdictional agency to certify Class II
Medical Devices in the European Union. Additionally, Nexera has pending approval of two
additional SpectraShieldô respirator masks as Reusable FFP2 and Reusable FFP3
antimicrobial respirators masks, the first and only masks of their kind in the world. These
masks are planned to be commercially available by the end of October in the European Union
and other countries that accept or recognize the CE Mark.
<br /><br />

In March 2011, the company announced that its SpectraShieldô 9500 Surgical N95 Surgical
Respirator Mask with embedded antimicrobial technology became the first antibacterial
respirator mask to receive FDA 510K and NIOSH clearance for the commercial market. The
SpectraShieldTM Respirator begins to kill certain strains of bacteria, including Methicillinresistant
Staphylococcus aureus (MRSA), Streptococcus, and other harmful and deadly
bacteria immediately on contact.
<br /><br />

Nexeraís president, Paul Sallarulo, stated, ìWe are pleased to continue take the necessary
steps to provide people with the best antimicrobial technology and best respiratory protection
available today.î He added, ìPeople have very little protection in the event of a healthcare
crisis involving airborne pathogens, and our SpectraShieldô Respirator can provide a person
with the highest degree of protection.î
<br /><br />

The SpectraShieldô Respirator is designed to capture harmful germs and kill them when they
are on the surface of the mask. The respirator also traps and kills any additional germs in the
middle filtration layers to prevent them from entering the body and causing an infection. The
SpectraShieldô Respirator utilizes a revolutionary antimicrobial technology called FosshieldÆ,
made in the USA by Foss Manufacturing. This antimicrobial technology is currently in use in
many industries from hospitality, to healthcare, to apparel.
<br /><br />

The SpectraShieldTM Respirator is available in adult sizes. This revolutionary next generation
respirator is designed to give the general public, health officials, first responders, and care
givers the best available protection against infectious diseases.
<br /><br />

Nexera plans to roll out a portfolio of other infectious disease preventing textile-based
healthcare products made from FosshieldÆ technology, including other types of surgical and
dental masks, mattress pads and covers, warming blankets, hospital uniforms, lab coats,
wound care products, and hospital divider curtains to name a few. All of these solutions are
designed to assist hospitals in controlling or reducing ìhospital acquired infections.î This
revolutionary FosshieldÆ fiber technology is ideally suited for infectious disease prevention,
thereby assists in reducing the spread of infectious diseases in any environment.
<br /><br />

About Nexera:
Nexera Medical Inc. is headquartered in Fort Lauderdale, Florida. The company is engaged in
the development, manufacturing, and distribution of antimicrobial respirator and surgical masks,
as well as other planned antimicrobial textile based medical products such as medical slings,
surgical site aprons and hospital linen products worldwide. All of Nexera's products are
designed to provide maximum protection against microorganisms such as bacteria, virus and
fungus using FosshieldÆ patented technology. Nexera partners with governments and leaders
in the health care industry in the fight against viral pandemic and other health care crises. To
learn more, visit www.nexeramed.com.
<br /><br />

About Foss Manufacturing:
FosshieldÆ fabric is manufactured by Foss Manufacturing, LLC (Foss) based in Hampton, New
Hampshire. Foss is a well-known leader and pioneer in the use of non-woven fabrics and
specialty synthetic fibers in an ever-expanding number of environments and applications.
Using new technologies and state-of-the-art computerized production, it is unrivaled in its ability
to efficiently and effectively design, manufacture, convert, package, and distribute a vast array
of specialty textile products worldwide. Foss is one of the world's largest needle-punch based
manufacturers, and it has the expertise and experience to support customers' needs across
virtually all applications and markets. To learn more, visit <a href="http://www.fossmfg.com" target="_blank">www.fossmfg.com</a>.
