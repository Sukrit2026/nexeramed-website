<div class="hdtitlemain">Nexera Medical Receives Simultaneous FDA 510K and NIOSH Clearance of New SpectraShield™ Antibacterial N95 Respirator Mask Using Fosshield® Technology
</div><br>


<strong>March 8, 2011</strong> - Fort Lauderdale, Florida – Nexera Medical, Inc. (“Nexera”) announced that it has received FDA 510K and NIOSH clearance to commercially market their SpectraShield&trade; Antibacterial N95
Respirator Mask (SpectraShield&trade;). Nexera, a leading developer in microbial fighting textile based
healthcare products has introduced this new antibacterial respirator mask technology. Considered
the model for the next generation respirator mask, the SpectraShield&trade; respirator mask is equivalent
to a N95 (NIOSH rating), or a FFP3 (E.U. criteria) respirator mask. The SpectraShield&trade; mask
begins to kill certain strains of bacteria immediately on contact, and is available in both adult and
children’s sizes. This revolutionary next generation mask is designed to give the general public,
health officials, first responders, and care givers the best available protection against infectious
diseases.
<br /><br />

“Nexera developed this technology in response to existing health threats and the possibility
of new strains of harmful microorganisms,” said Paul Sallarulo, Nexera President and CEO. “We
understand the need for governments and public health officials to have access to cutting edge
technology that will help them prepare for and respond to future disease outbreaks and/or
pandemics.”
<br /><br />

The SpectraShield&trade; is an innovative, antibacterial mask that protects against, and actually
kills bacteria with which it comes into contact. Through the use of Fosshield® fiber system, a
patented technology which permanently embeds a non-toxic active silver and copper antimicrobial
agent into the fibers of the material, bacteria is unable to build up on the outer layers of the mask nor
penetrate it over time. Independent testing in the United States has shown the mask’s ability to kill
and inhibit the growth of a wide variety of bacteria, including Methicillin-resistant Staphylococcus
aureus (MRSA), Streptococcus, and other harmful and deadly bacteria. In addition, independent
testing in Europe has shown the SpectraShield&trade; mask to inactivate a number of viruses by 99.9%.
In developing the SpectraShield&trade; mask, Nexera entered into a collaborative research and
development relationship with Foss Manufacturing in Hampton, NH (“Foss”), the owner of the
patented Fosshield® technology in return for exclusive rights to use Fosshield® in all textile-based
healthcare products.
<br /><br />

As governments work to guard against potential health threats, and while medical facilities
are turning their focus to reducing hospital acquired infections, the SpectraShield’sTM one-of-a-kind
technology serves as both a protective and cost-effective solution. The unique dual filtration and
long-lasting antibacterial characteristics of the mask protect both users from becoming infected and
the general public from being exposed to disease by an infected user. The SpectraShield&trade; mask was
tested against bacteria in an extended use test, which the mask was exposed to various conditions
replicating real use over a 12 hour period. The SpectraShield&trade; mask passed the prerequisite
threshold set by the FDA for antibacterial kill performance.
<br /><br />

Almost all government stockpiles must have on hand four to five masks per person, per day
because most masks available today may need to be replaced and disposed of every three to five
hours. In contrast, the SpectraShield&trade; mask can be continuously worn over a 12 hour period, saving
money and reducing the number of masks a government or medical facility must have on hand.
Nexera has also led the way in developing masks for children, which are currently in short
supply. “Children are one of the most vulnerable groups during outbreaks but there are too few
respirator masks made that will fit them properly,” said Dr. Sheldon Warman, an advisor and Board
Member of Nexera.
<br /><br />

Prior to receiving FDA and NIOSH clearance allowing commercialization of the mask in the
United States, the SpectraShield&trade; mask received regulatory approval in Canada and the European
Union for an antimicrobial version of the SpectraShield&trade; mask as a N99 respirator for Canada, or a
FFP3 respirator for Europe. The SpectraShield&trade; mask has been used by officials in Central and
South America, the Middle East, and Europe, including the Pan American Health Organization
(PAHO) which purchased masks for their field officers in Mexico and Panama who were responding
to the recent Swine Flu outbreak.
<br /><br />

Nexera plans to rollout a portfolio of other textile based healthcare products made from
Fosshield® technology, including other types of surgical and dental masks, mattress pads and covers,
warming blankets, hospital uniforms, lab coats, wound care products, and hospital divider curtains,
to name a few. All which are designed to assist hospitals in controlling or reducing “hospital
acquired infections. This revolutionary Fosshield® fiber technology is ideally suited for infectious
disease prevention.
<br /><br />

SpectraShield&trade; is a registered trademark of Nexera Medical, Inc.
<br /><br />

<em>Nexera Medical Inc. is headquartered in Fort Lauderdale, Florida. The company is engaged
