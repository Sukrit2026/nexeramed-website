<u>Preparing for Broad Spectrum Protection in a Public Health Emergency</u><br><br>

<div class="hdtitlemain">Nexera Medical Obtains a Medical Device Establishment License for SpectraShield 9900
Antibacterial Respirator Mask in Canada
</div><br>

<i>Only Face Mask Proven to Continuously Kill Bacteria
and Provide Superior Filtration to N95 Masks</i>
<br><br>

<strong>Vancouver, BC, September 4, 2007</strong> - Nexera Medical, Ltd., developer, manufacturer and distributor of antimicrobial textile products worldwide, announced today that it has been granted a Medical Device Establishment License by Health Canada for distribution of the SpectraShield 9900 respirator mask.  SpectraShield 9900 is the only N99 face mask proven to continuously kill bacteria and fight microorganism contamination with filtration superior to N95 masks.  Now available in Canada, the SpectraShield 9900 is intended to be used as a front line defense against infectious and contagious diseases. 
<br><br>

"The approval of SpectraShield 9900 in Canada marks a significant milestone for our company, and we look forward to supplying healthcare organizations, government agencies, first-responders and the public with this superior product," said Marci Haas, President and Chief Executive Officer of Nexera Medical, Inc.  "We believe there are no other face masks today that are approved to provide 'dual-action', broad-spectrum protection which may be critical in the event of a major public health crisis in Canada.  We will continue to enhance our product portfolio to meet the needs of a growing demand for bio-protection products."
<br><br>

Independent studies were conducted at Nelson Laboratories and the University of Arizona under the guidance of Charles P. Gerba, Ph.D., Department of Soil, Water and Environmental Science.
<br><br>

"Results from the studies showed that SpectraShield 9900 respirator mask inactivates a broad spectrum of bacteria capable of airborne transmission to levels below detection within one hour, and in some instances less than 10 minutes, and also exhibits anti-viral activity," said Dr. Gerba.  
<br><br>

With tested filtration performance equivalent to an N99 (meaning 99% of contaminants are trapped in the filter and do not penetrate the mask), the SpectraShield 9900 mask exceeds the United States Center for Disease Control and World Health Organization guidelines for protection against infectious microbial threats such as Avian Influenza, SARS and Tuberculosis.
<br><br>

Studies, conducted over the past year, were designed to evaluate the mask's antimicrobial properties on a number of bacterial microorganisms including Escherichia coli, streptococcus pyogenes, methicillin resistant staphylococcus aureus, legionella pneumophila, haemophilus influenza, and coliphage MS-2.  Varying inoculation concentrations of 0 to 1,000,000 units were tested within a time span of 0 - 60 minutes. Results indicated that 99.99 to 99.999% (or to five log reduction) of the bacterial microorganisms were killed within one hour.  More extensive evaluation studies are also underway for viral microorganisms.
<br><br>

SpectraShield 9900 is manufactured using a patented technology; antimicrobial fibers used in the mask incorporate a non-toxic silver-copper ionic agents proven safe for extended contact with individuals. Due to SpectraShield 9900's antimicrobial properties, odor caused by bacterial growth is also eliminated.  
<br><br>

While SpectraShield 9900 is approved in Canada for commercialization, currently Nexera Medical, Inc. is in the process of applying for regulatory approvals in other markets including the United States, European Union, Australia and Japan. Nexera Medical Inc. has already taken orders in several other countries around the world.
<br><br>

<strong>About Nexera Medical Ltd.</strong><br>
Nexera Medical Ltd., a wholly owned subsidiary of Nexera Medical Inc., is engaged in the development, manufacturing and distribution of antimicrobial respirator and surgical masks, as well as other antimicrobial textile based medical products such as medical slings, surgical site aprons and hospital linen products worldwide.  The company's mission is to create high-quality, cutting-edge health related medical products designed to safeguard and enhance the quality of human life. All of Nexera's products are designed to provide maximum protection against microorganisms such as bacteria, virus and fungus using FosshieldÛ patented technology.  Nexera partners with governments and leaders in the health care industry in the fight against viral pandemic and other healthcare crises. For more information, visit www.nexeramed.com.
<br><br>

