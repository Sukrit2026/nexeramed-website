<h1 class="hdtitlemain">Respirator Masks</h1>




<div style="float: right; padding: 20px 15px 20px 15px;"><img src="../graphics/img_masks.jpg" width="400" height="400" border="0" alt="Respirator Masks"></div>

With tested filtration performance equivalent to an N99 (meaning 99% of contaminants are trapped in the filter and do not get through the mask), the SpectraShield&trade; mask meets the guidelines for infectious disease exposure control established by the World Health Organization ("WHO") and the Center for Disease Control ("CDC").
<br /><br />

The SpectraShield&trade; is scientifically proven to combat the growth of harmful microorganisms and biological pathogens and should be considered the number one choice in personal protection.  Every family should have a stockpile of SpectraShield&trade; masks at home.
<br /><br />

Some antimicrobial masks require frequent changing of the filtration cartridges at an additional cost where this is not necessary with the SpectraShield mask.  Easily and hands-down, no other antimicrobial mask can compare to the performance of the SpectraShield&trade; Series of mask.

