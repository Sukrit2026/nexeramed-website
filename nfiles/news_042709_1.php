<div class="hdtitlemain">Swine Flu Outbreak Prompts Nexera Medical to Donate Antimicrobial Reusable N99 Respirator Masks to a Regional Office of The World Health Organization
</div><br>

<strong>FORT LAUDERDALE, FL April 27, 2009</strong> - Nexera Medical, Inc. ("Nexera") today announced that it has made a donation of its SpectraShield™ antimicrobial N99 respirator masks to the Pan American Health Organization ("PAHO") in Washington, DC, a regional office of the World Health Organization ("WHO").  Nexera is sending the SpectraShield™ masks to several locations including Mexico and Panama as an effort to protect personnel that are being deployed into areas infected by the recent outbreak of the Swine Flu.  PAHO had originally made a purchase of these SpectraShield™ masks, when Nexera contributed an additional quantity as a donation.
<br><br>

<cfif session.regionsiteid EQ "us"><cfelse>
<strong>About the SpectraShield N99 Reusable Respirator Mask:</strong>  The SpectraShield™ mask is the only commercially available antimicrobial respirator mask made from Fosshield® technology incorporating silver and copper zeolite compounds which are permanently embedded into the fibers of the mask.  The SpectraShield™ mask was subject to rigorous microbial testing for bacteria and viruses in the U.S. and Europe, while the tests were preformed in specific government certified laboratories.  The final test results were shown to kill or inactivate a variety of bacteria and viruses, up to 99.9%, including the Swine Flu, the Bird Flu, SARS, Tuberculosis, Influenza A, MRSA, and more.  The SpectraShield™ mask has also been tested for reusability and can be worn up to 28 days without loosing its filtration or antimicrobial efficacy.  The SpectraShield™ mask has received regulatory approval and a CE mark in the European Union, and has received a license for sale in Canada, but is not yet available in the U.S.
<br><br>
</cfif>

<strong>About the Nexera Medical, Inc.:</strong>  Nexera, is a privately held company located in Fort Lauderdale, FL and is the developer and manufacturer of antimicrobial textile medical devices.  Nexera holds a technology license for the exclusive worldwide use of Fosshield® in any medical device that can be manufactured from Fosshield® technology and fibers.  Fosshield® is a proprietary patented antimicrobial fiber and fabric technology developed and owned exclusively by Foss Manufacturing, LLC, located in Hampton, NH.
<br><br>

<strong>About Foss Manufacturing, LLC:</strong>  Foss Manufacturing, LLC ("Foss") is a well-known leader and pioneer in the use of non-woven fabrics and specialty synthetic fibers in an ever-expanding number of environments and applications.  Using new technologies and state-of-the-art computerized production in Hampton, NH, it is unrivaled in its ability to efficiently and effectively design, manufacture, convert, package, and distribute a vast array of specialty textile products worldwide.  Foss is one of the world's largest needle-punch based manufacturers, and it has the expertise and experience to support customers' needs across virtually all applications and markets.
<br><br>

For more information about Nexera or the SpectraShield™ mask, go to Nexera's website at <a href="http://www.nexeramed.com">www.nexeramed.com</a>, or call its headquarters at 954-495-2020.  For more information about Fosshield® or Foss Manufacturing, go to Foss' website at <a href="http://www.fossmfg.com" target="_blank">www.fossmfg.com</a>, or call its headquarters at 603-929-6000.
