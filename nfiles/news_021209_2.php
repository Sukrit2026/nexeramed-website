<div class="hdtitlemain">Nexera Medical, Inc. files with the U.S. Food and Drug Administration ("FDA") for the SpectraShield 9900 as an antimicrobial surgical mask...
</div><br>

<strong>February 12, 2009 </strong> - Nexera Medical, Inc. files with the U.S. Food and Drug Administration ("FDA") for the SpectraShield 9900 as an antimicrobial surgical mask.  The application is submitted under the FDA's 510K approval process for medical devices.  Nexera successfully completed a series of tests mandated by the FDA including safety in the breathing zone, dermal sensitivity tests, toxicity tests, and more.  Nexera is confident that they are taking a step in the right direction to eventually have the SpectraShield 9900 become approved as an antimicrobial respirator mask.  
<br><br>

According to Paul Sallarulo, Nexera's President and Chief Executive Officer, he states that; "An FDA approval of SpectraShield 9900 in the United States would be a major milestone for the company and would open the door for sales of the SpectraShield 9900 antimicrobial mask in the U.S., and into other countries around the world that would recognize the FDA approval". 
<br><br>

SpectraShield 9900 is manufactured using a patented technology; antimicrobial fibers used in the mask incorporate non-toxic silver-copper ionic agents proven safe for extended contact with individuals.  Due to SpectraShield 9900's antimicrobial properties, the SpectraShield 9900 continuously self-sanitizes itself for extended use. 
<br><br>

<strong>About Nexera Medical, Inc.</strong>  Nexera Medical Inc. is engaged in the development, manufacturing and distribution of antimicrobial respirator and surgical masks, as well as other antimicrobial textile based medical products such as medical slings, surgical site aprons and hospital linen products worldwide.  The company's mission is to create high-quality, cutting-edge health related medical products designed to safeguard and enhance the quality of human life. All of Nexera's products are designed to provide maximum protection against microorganisms such as bacteria, virus and fungus using FosshieldÛ patented technology.  Nexera partners with governments and leaders in the health care industry in the fight against viral pandemic and other healthcare crises. For more information, visit www.nexeramed.com.
<br><br>

