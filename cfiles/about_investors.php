<h1 class="hdtitlemain">Shareholder Update: March 2011</h1>

Thank you for your participation as an investor and shareholder of Nexera Medical Inc. We are very
pleased to announce that in February 2011 Nexera <strong>received both FDA 510K and NIOSH clearance</strong> to
commercially market our SpectraShield&trade; 9500 Surgical N95 Respirator in the United States. Attached is Nexera's
press release with the FDA announcement.
<br /><br />

Nexera, a leading developer in microbial fighting textile based healthcare products has introduced this new
antibacterial surgical respirator mask technology. Our mask is now considered as the model for the next generation
of respirator masks. The mask begins to kill certain strains of bacteria immediately on contact. Our revolutionary
next generation mask is designed to give the general public, health officials, first responders, and care givers the
best available protection against infectious diseases.
<br /><br />

Independent testing in the United States has shown the SpectraShield&trade; Antibacterial N95 Respirator
Mask's ability to kill and inhibit the growth of a wide variety of bacteria and has been certified in the USA to kill
Methicillin-resistant Staphylococcus aureus (MRSA), Streptococcus pyogenes, and Haemophilus influenzae. The
mask was tested against bacteria in an extended use test, with the mask being exposed to various conditions
replicating real use over a 12 hour period. We are forging ahead with additional virus and reusability regulatory
approvals in the United States.
<br /><br />

In addition, independent testing in Europe has shown the SpectraShield&trade;ask to inactivate a number of
viruses by 99.9%. In developing the SpectraShieldTM mask, Nexera entered into a collaborative research and
development relationship with Foss Manufacturing in Hampton, NH ("Foss"), the owner of the patented
Fosshield&reg; technology in return for exclusive rights to use Fosshield&reg; in all textile-based healthcare products.
<br /><br />

As governments work to guard against potential health threats, and while medical facilities are turning their
focus to reducing hospital acquired infections, the SpectraShield'sTM one-of-a-kind technology serves as both a
protective and cost-effective solution. The unique dual filtration and long-lasting antibacterial characteristics of the
mask protect both users from becoming infected and the general public from being exposed to disease by an
infected user. Almost all government stockpiles must have on hand four to five masks per person, per day because
most masks available today may need to be replaced and disposed of every three to five hours.
<br /><br />

Nexera has established a strategic alliance with American Medical Depot (our U.S. Master Distributor), a
multi-million commercial enterprise with three distribution centers, and being recognized as preferred medical
supply vendor to the U.S. Military and various other U.S. Government Agencies. Nexera plans to rollout a
portfolio of other textile based healthcare products made from Fosshield&reg; technology, including other types of
surgical and dental masks, mattress pads and covers, warming blankets, hospital uniforms, lab coats, wound care
products, and hospital divider curtains, to name a few. All which are designed to assist hospitals in controlling or
reducing "hospital acquired infections. This revolutionary Fosshield&reg; fiber technology is ideally suited for
infectious disease prevention.
<br /><br />

We believe Nexera's "SpectraShield&trade; Plus" Antimicrobial Respirator Mask - N95 and N99 is the only antibacterial and antimicrobial respirator masks in the market place. We are all very excited about the enormous potential and can now actively market our products worldwide.
<br /><br />
