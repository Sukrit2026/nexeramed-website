<strong>Independent Testing and Methods</strong><br><br>

<strong>AATCC:</strong>  This represents The American Association of Textile & Colorists.  The AATCC is the world's largest technical and scientific organization to promote the knowledge and applications within the textile industry.
<br /><br />

<strong>AATCC Test Method 30:</strong>  The two purposes of this test method are to determine the susceptibility of the textile materials to mildew and rot and to evaluate the efficacy of fungicides on textile materials.
<br /><br />

<strong>AATCC Test Method 100:</strong>  This test method provides a quantitative procedure for the evaluation of the degree of antibacterial activity.  Assessment of antibacterial activity finishes on textile material is determined by the degree of antibactieral activity intended in the use of such materials.  If only bacteriostatic activity (inhibition of multiplication) is intended, a qualitative procedure which clearly demonstrates antibacterial activity as contrasted with lack of such activity by an untreated specimen may be acceptable.  However, if bactericidal activity is intended or implied, quantitative evaluation is necessary.  Quantitative evaluation also provides a clearer picture for possible uses of such treated textile materials. 
<br /><br />

<strong>ASTM:</strong> American Society for Testing and Materials. ASTM is a non-governmental organization committed to the development of test methods and voluntary standards for materials, products, systems and services.
<br /><br />

<strong>ASTM 2149:</strong>   This test method is designed to evaluate the resistance of non-leaching antimicrobial treated specimens to the growth of microbes under dynamic contact conditions. This dynamic shake flask test was developed for routine quality control and screening tests in order to overcome difficulties in using classical antimicrobial test methods to evaluate substrate-bound antimicrobials.
<br /><br />

<strong>ASTM 1671:</strong>   This test method is used to measure the resistance of materials used in protective clothing to penetration by bloodborne pathogens using a surrogate microbe under conditions of continuous liquid contact. Protective clothing material pass/fail determinations are based on the detection of viral penetration.
Nelson technical description of the test is: This test procedure is used for evaluating the viral penetration resistance characteristics of protective clothing materials, which are used to protect against bloodborne pathogen hazards.  This test procedure was designed to comply with the ASTM F1671 procedure developed by ASTM Subcommittee F23.40 on Biological Hazards.  The test device used in this procedure is the ASTM F903 Chemical Penetration Cell.
<br /><br />

<strong>ASTM 1670:</strong>  This test method is used to measure the resistance of materials used in protective clothing to penetration by bloodborne pathogens using a surrogate microbe under conditions of continuous liquid contact. Protective clothing material pass/fail determinations are based on the detection of viral penetration.  
<br /><br />

Nelson technical description of the test is:  This test procedure is used for evaluating the synthetic blood penetration resistance characteristics of protective clothing materials, which are used to protect against bloodborne pathogen hazards.   This test procedure was designed to comply with ASTM Method F1670 developed by the ASTM Subcommittee F23.40 on Biological Hazards.  This procedure should be used as a preliminary screen for the evaluation of protective clothing materials designed to provide protection against bloodborne pathogen hazards.  Protective clothing materials which pass ASTM Method F1670 test should also be tested against the more sensitive ASTM Method F1671 Viral Penetration Test.  The test device used for this procedure is the ASTM F903 Chemical Penetration Cell.
<br /><br />

<strong>ASTM 1819:</strong> This test method is for Resistance of Materials Used in Protective Clothing to Penetration by Synthetic Blood Using a Mechanical Pressure Technique (F 1819) is a new barrier test method that applies predominately mechanical pressure to the material and synthetic blood until penetration is visible. A modified version of F 1819 uses a sensor test head to detect penetration. ASTM Test Method for Resistance of Materials Used in Protective Clothing to Penetration by Synthetic Blood (F 1670) applies 13.8 (2 psi) of predominately hydrostatic pressure to the fabric, producing a pass/fail penetration result. A modified version of F 1670 applies different amounts of hydrostatic pressure to the fabric in sequential increments for 30 seconds until penetration occurs. The barrier properties of 14 different materials were measured using these methods. The results indicated that materials reinforced with films, coatings, or membranes passed F 1670 and reached the highest pressure level on F 1819 and modified F 1670 without penetration. Single-layer and fabric reinforced gowns exhibited varying degrees of resistance to synthetic blood. Results of the mechanical pressure test and the modified hydrostatic test were highly correlated (r = 0.99). These methods provide more information on the barrier properties of materials that fail F 1670.
<br /><br />

<strong>Nelson Laboratories CTX101:</strong> this is for cytotoxicity testing in an agar overlay:  USFDA (21 CFP Part 58)  Cytotoxicity is a rapid, standardized test that is very sensitive and is an inexpensive way to determine if the materials in that of a medical device contain significant quantities of harmful extractables and their effect on cellular components.  Required for all types of medical devices.  Cellular toxicity is covered in ISO 10993-5.  For agar overlay media is placed on top of a monolayer of L-929 cells, and a sample is placed on top of the agar media then incubated.  For MEM elution the sample is place in contact with the monolayer of L-929 cells and then incubated.  In both methods the cells are scored for cytopathic effect. 
<br /><br />

<strong>Nelson FTS101:</strong>  This is for flammability testing  (16CFR Part 16101).  This test method is designed to comply with 16 CFR Part 1610.  The ASTM F 2100 (Standard Specification for Performance of Materials Used in Medical Face Masks) requires that all face masks meet the requirements for Class 1 flammability.  This method is appropriate for a variety of medical textiles, including surgical gowns and drapes.
