<div class="hdtitlemain">Nexera Medical Receives European Union Clearance and CE Mark for
the SpectraShieldô FFP2 RD and FFP3 RD REUSABLE Antimicrobial
Respirator Mask ñ the first ever to receive this clearance
</div><br>

<strong>FORT LAUDERDALE, Florida (November 7, 2011))</strong> ñ Nexera Medical, a leading developer of
microbe-fighting textile-based healthcare products, receives EN 149: 2001 approval for its
SpectraShieldô FFP3 RD and FFP2 RD <u><strong>Reusable</strong></u> Antimicrobial respirator masks from INSPEC
International Ltd., a European Union certified testing facility located in Greater Manchester, United
Kingdom. The tests were performed on the SpectraShieldô FFP3 RD and FFP2 RD in simulation of
the practical use of the apparatus for reusability, including the initial "clogging test" using dolomite
dust to emulate severe real-time conditions for a user. The dolomite testing method is exclusively
used for measuring filtration performance for determining reusable efficiency. Both, the FFP2 RD
and FFP3 RD masks met all of the filtration, inhalation, and exhalation requirements specific to each
category of mask and met all the requirements for effective antimicrobial performance to earn both
the reusable and antimicrobial classification for these masks. The "RD" after the FFP2 or FFP3
designates its reusability status earned by the "dolomite" testing method.
<br /><br />

The SpectraShieldô mask begins to inactivate or kill certain strains of viruses and bacteria
immediately on contact and continuously kills or inactivates over long periods of time. This
revolutionary next generation mask is designed to give the general public, health officials, first
responders, and care givers the best available protection against infectious diseases. The
antimicrobial aspects were recognized by the test results received from the Health Protection Agency
against bacteria on November 8, 2008 and from RetroScreen Virology Ltd. against viruses on
January 22, 2009.
<br /><br />

Considered as the next generation respirator masks, these are the first ever FFP2 RD or FFP3 RD
masks to receive both a reusable and an antimicrobial designation, differentiating them from all
other respirator masks in the market <strong>as they are reusable for periods of up to 14 days</strong>. These
SpectraShieldô masks stand alone as the only reusable antimicrobially-protected respirator masks
for the healthcare industry. Paul Sallarulo, President and Chief Executive Officer, stated;
"Receiving this approval and CE Mark for the SpectraShieldô FFP2 RD and FFP3 RD continues our
long-term objective of developing products designed to save lives and protect people in a widespread
health crisis." Furthermore, he says; "We understand the need for governments and public health
officials to have access to cutting edge technology that will help them prepare for and respond to
future disease outbreaks and/or pandemics."
<br /><br />

SpectraShieldô masks are manufactured using a patented technology of FosshieldÆ antimicrobial
fibers used in the mask, which incorporate non-toxic silver-copper ionic agents that are proven safe
and effective for extended contact with individuals. Due to SpectraShieldô antimicrobial properties,
the SpectraShieldô FFP2 RD and FFP3 RD masks continuously self-sanitizes themselves for
extended or repeat use.
<br /><br />

The SpectraShieldô FFP2 RD and FFP3 RD will be marketed through independent medical product
distributors throughout Europe and neighboring countries as the "GUARDIAN" model.
<br /><br />

SpectraShieldô is a registered trademark of Nexera Medical, Inc.
